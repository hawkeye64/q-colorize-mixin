<template>
  <div class="q-pa-md">
    <div class="q-pb-md q-gutter-md row justify-around">
      <q-input
        filled
        v-model="borderColor"
      >
        <template v-slot:append>
          <q-icon name="colorize" class="cursor-pointer">
            <q-popup-proxy transition-show="scale" transition-hide="scale">
              <q-color v-model="borderColor" />
            </q-popup-proxy>
          </q-icon>
        </template>
      </q-input>
    </div>
    <q-separator />
    <div class="q-pa-md row justify-around">
      <my-component
        :border-color="borderColor"
      >
        <div class="text-h4">Some Text</div>
      </my-component>
    </div>
  </div>
</template>

<script>
import Vue from 'vue'
import { QColorizeMixin } from 'ui'

const MyComponent = Vue.extend({
  name: 'my-component',

  mixins: [QColorizeMixin],

  props: {
    // color prop is added by colorize mix-in
    // background-color is optional and needs to be added if needed
    borderColor: String
  },

  render (h) {
    return h('div', this.setBorderColor(this.borderColor, {
      style: {
        width: '200px',
        height: '200px',
        borderStyle: 'solid',
        borderWidth: '10px'
      }
    }))
  }
})

export default {
  components: {
    MyComponent
  },

  data () {
    return {
      borderColor: '#00FAFA'
    }
  }
}
</script>
